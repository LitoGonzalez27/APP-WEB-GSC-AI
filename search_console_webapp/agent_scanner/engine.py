# Checks cuyo "0" significa "no lo he encontrado". Si el sitio nos ha bloqueado,
# ese 0 no es un hallazgo sobre la web: es una ceguera nuestra, y afirmarlo sería
# mentir. Se degradan a "no verificable" en vez de puntuar como fallo.
CHECKS_POR_AUSENCIA = {"1.4", "1.5", "1.7", "2.1", "2.3", "3.1", "3.2", "3.3",
                       "3.4", "3.5", "3.6", "4.5", "5.1", "5.2", "5.3", "5.5",
                       "5.6", "6.1", "6.2", "7.1", "7.5", "7.6"}
# Se quedan FUERA a propósito aunque el sitio bloquee: 1.6, 2.4 y 4.4 miden
# exactamente esa hostilidad al acceso automatizado, que es lo que un agente
# sufriría. Que el sitio nos bloquee ES el hallazgo, no ruido.

# Checks que leen MARCADO (JSON-LD, formularios, atributos). Si la home vino por
# un fallback de texto (Jina), el marcado sencillamente no está en lo que vemos:
# reportar "no tiene datos estructurados" sería describir nuestra vía de acceso,
# no la web del cliente.
CHECKS_DE_MARCADO = {"3.1", "3.2", "3.3", "3.4", "3.5", "3.6", "4.2", "6.2",
                     "7.1", "7.2", "7.5", "7.6"}

# Checks cuyo "no encontrado" depende de SONDAS adicionales (robots, sitemap,
# .well-known, negociación de contenido), no de la portada. Si la portada se vio
# pero esas sondas recibieron bloqueos, solo estas ausencias son inafirmables.
CHECKS_DE_SONDA = {"1.4", "1.5", "2.1", "4.5", "5.5", "5.6", "6.1"}


# Checks que NO pueden responderse sin abrir al menos una ficha de producto.
# 7.3 se queda fuera: mira el PSP en la home, no necesita ficha.
CHECKS_DE_FICHA = {"3.3", "4.2", "7.1", "7.2", "7.5", "7.6"}


def _degradar_sin_ficha_producto(results, ctx):
    """Un e-commerce cuyo catálogo no hemos sabido alcanzar no puntúa 0.

    Caso real (batería 5): zalando.de. Nuestros patrones de URL eran ES/EN, sus
    rutas son alemanas, y el muestreo terminó con buckets producto=0. Los checks
    de ficha se puntuaron 0 y el informe afirmaba "Sin JSON-LD Product en fichas
    muestreadas", "Ninguna ficha de producto accesible" y "Sin informacion de
    envio detectable, ni estructurada ni visible. Un agente que compare opciones
    descartara esta tienda" — sobre páginas que NUNCA abrimos. El score salía
    14.4 con score_fiable=True, listo para entregarse a un cliente.

    Ampliar los patrones (hecho) reduce el caso, pero no lo elimina: siempre
    habrá tiendas cuyo catálogo no alcancemos. La regla de fidelidad no puede
    depender de acertar el idioma. Si no vimos ficha, no afirmamos nada de ella.
    """
    if not ctx.get("sin_ficha_producto"):
        return results
    motivo = ("no se alcanzó ninguna ficha de producto en el muestreo, así que no "
              "es posible afirmar qué contienen las fichas de esta tienda. No es "
              "un defecto observado de la web: es una limitación de nuestra "
              "cobertura del catálogo")
    n = 0
    for r in results:
        if r["id"] in CHECKS_DE_FICHA and r["score"] == 0:
            r["score"] = None
            r["manual"] = True
            # marca explícita: el scoring necesita distinguir "aquí no hay nada
            # que medir" (no aplica, su peso se reparte) de "no hemos podido
            # medirlo" (no se reparte, o inflaría lo poco que quede en pie)
            r["no_verificable"] = True
            r["evidence"] = f"NO VERIFICABLE — {motivo}. (Sonda: {r['evidence'][:120]})"
            n += 1
    if n:
        ctx["cobertura_producto_degradada"] = {"degradados": n, "motivo": motivo}
        ctx["trail"].append({
            "step": "guardarraíl de cobertura de catálogo", "status": "warn",
            "detail": f"{n} checks de ficha degradados a 'no verificable'. {motivo}. "
                      f"La puntuación de C7 cubre solo lo comprobable desde la portada"})
    return results


def _cobertura_ciega(ctx):
    """¿Nos han servido una cáscara en lugar de la web?

    Caso real (batería 5, zalando.de en la segunda pasada): HTTP 200, 11 KB de
    portada, robots.txt correcto, _human=200… y CERO enlaces internos, CERO
    páginas muestreadas, CERO señales de tipología. El motor ya avisaba ("SIN
    señales: la web devuelve poco/ningún contenido a accesos automatizados")
    y aun así entregaba score 20.3 con score_fiable=True: todos los checks de
    contenido a 0 "por ausencia" sin haber visto una sola página.

    `_degradar_si_bloqueado` no lo cazaba porque solo mira que la portada pese
    >=500 bytes y que el UA humano reciba 200 — y una cáscara cumple ambas.
    La señal buena no es el peso: es que no hay NADA que rastrear.
    """
    if not ctx.get("cobertura_ciega"):
        return False
    ev = ctx.get("typology_evidence") or {}
    sin_senales = ((ev.get("ecommerce") or {}).get("puntos", 0) == 0
                   and (ev.get("saas") or {}).get("puntos", 0) == 0)
    return not ctx.get("pages") and sin_senales


def _veredicto_de_acceso(ctx):
    """¿Qué podemos AFIRMAR sobre el acceso, con la evidencia que tenemos?

    Antes esto era un único "puerta cerrada a agentes" que se disparaba solo con
    que nuestro UA de navegador no recibiera contenido — ignorando la matriz de
    bots, que ya tenía la mitad de la respuesta. Caso real: argal.com y noel.es
    salían "puerta cerrada" desde el servidor de producción y desde otra red
    servían 136 KB y 85 KB sin problema. Estábamos describiendo el bloqueo de un
    rango de IPs como si fuera la política del cliente hacia los agentes.

    Tres situaciones distintas:
      puerta_cerrada : vimos la web con navegador Y vimos que rechaza a los bots
                       de IA. Hallazgo agéntico, con las dos mitades probadas.
      no_evaluable   : no la pudimos leer, y los bots tampoco. No se distingue
                       "bloquea toda automatización" de "bloquea nuestra IP", así
                       que no se afirma ninguna de las dos.
      None           : se pudo leer (por navegador o rescatada como bot) y la
                       nota va por la escala normal.
    """
    bm = ctx.get("bot_matrix") or {}
    bots = {k: v for k, v in bm.items() if k != "_human"}
    algun_bot_entra = any(v == 200 for v in bots.values())
    humano_entra = bm.get("_human") == 200

    if humano_entra and bots and not algun_bot_entra:
        return "puerta_cerrada"
    if (ctx.get("acceso_degradado") or {}).get("nivel") == "total":
        return "no_evaluable"
    return None


def _degradar_si_bloqueado(results, ctx):
    """Si no hemos podido ver el sitio (o solo en parte), no afirmamos ausencias.
    Distinguir 'no está' de 'no lo he podido mirar' es la diferencia entre un
    informe fiable y uno que difama al cliente. Tres niveles:
      total   → no vimos nada creíble: se degrada todo lo basado en ausencia.
      marcado → solo texto vía Jina: se degrada lo que lee HTML + las sondas.
      sondas  → la portada SÍ se vio; solo fallaron sondas posteriores: se
                degradan únicamente las ausencias que dependen de ellas, y la
                puntuación sigue siendo utilizable (con aviso en el trail).
    """
    human = (ctx.get("bot_matrix") or {}).get("_human", 0)
    via = (ctx.get("home") or {}).get("_via", "http")
    home_status = (ctx.get("home") or {}).get("status", 0)
    # El peso NO acredita que hayamos visto la portada: una página de bloqueo de
    # WAF pesa de sobra. Caso real (batería 5, mediamarkt.es): HTTP 403 a TODAS
    # las peticiones (incluido el UA humano) con 13.656 bytes de "Access
    # Denied". Como el cuerpo pasaba el listón de 500 bytes y la vía seguía
    # siendo "http", se caía en la rama `else` y se etiquetaba nivel "sondas",
    # cuyo texto afirma "la portada se obtuvo con normalidad" y deja
    # score_fiable=True. Resultado entregable: mediamarkt.es = 🔴 "Invisible
    # para agentes. Riesgo alto", score 20.8 marcado como fiable, calculado
    # sobre la página de error del WAF. Es exactamente el sesgo que perseguimos:
    # culpar a la web ajena de nuestra propia ceguera.
    # Una portada es creíble si el servidor la dio por buena (200) o si la
    # rescatamos por otra vía (render/jina), que sí sustituyen el cuerpo.
    # "bot:<nombre>" = la portada se rescató leyéndola como un bot de IA que SÍ
    # recibe 200. Es contenido real del sitio, y además el más pertinente para
    # esta auditoría: es lo que el sitio le sirve a una IA.
    via_creible = via in ("render", "jina") or via.startswith("bot:")
    home_ok = (len((ctx.get("home") or {}).get("body") or "") >= 500
               and (home_status == 200 or via_creible))

    ciego = _cobertura_ciega(ctx)
    # La portada vale si la vimos como navegador con 200, o si la rescatamos
    # leyéndola como un bot de IA al que el sitio SÍ responde.
    acceso_ok = ((human == 200 and via in ("http", "render"))
                 or via.startswith("bot:"))
    if home_ok and acceso_ok and not ciego:
        ctx["acceso_degradado"] = None
        return results

    if ciego:
        # La cáscara es indistinguible de una web pobre MIRANDO EL PESO, pero no
        # mirando lo que contiene: sin un enlace, sin una página y sin una señal
        # de tipología, no hemos visto la web. Se degrada como bloqueo total y
        # la puntuación deja de ser entregable.
        nivel, objetivo = "total", CHECKS_POR_AUSENCIA
        motivo = (f"lo que el sitio sirve a un acceso automatizado no contiene ni un "
                  f"enlace interno ni señal alguna de su tipo de negocio (HTTP {human}, "
                  f"{len((ctx.get('home') or {}).get('body') or '')} bytes). No es una "
                  f"web vacía: es que no estamos viendo la web, así que ninguna "
                  f"ausencia es afirmable")
    elif not home_ok or (human != 200 and via == "render"):
        # sin portada creíble (un render con el humano bloqueado puede ser la
        # página del captcha: no nos fiamos de ese HTML)
        nivel, objetivo = "total", CHECKS_POR_AUSENCIA
        motivo = (f"el sitio no sirve contenido a un acceso automatizado (UA humano "
                  f"recibe HTTP {human}). No es posible afirmar que esto falte: no "
                  f"hemos podido comprobarlo")
    elif via == "jina":
        nivel, objetivo = "marcado", CHECKS_DE_MARCADO | CHECKS_DE_SONDA
        motivo = ("la página solo se pudo leer vía jina, que devuelve texto sin "
                  "marcado, y el acceso directo está limitado. El HTML real no es "
                  "observable, así que no afirmamos nada sobre él")
    else:
        # portada vista por HTTP normal, pero sondas posteriores bloqueadas
        nivel, objetivo = "sondas", CHECKS_DE_SONDA
        motivo = (f"la portada se obtuvo con normalidad, pero el sitio devolvió "
                  f"HTTP {human} a sondas posteriores del análisis: las ausencias "
                  f"que dependen de esas sondas no son afirmables")

    n = 0
    for r in results:
        if r["id"] not in objetivo:
            continue
        # Hasta aquí solo se degradaba `score == 0`: la ausencia inafirmable.
        # Pero cuando el cuerpo que hemos leído NO es la web (página de bloqueo
        # del WAF), una PRESENCIA es igual de inafirmable que una ausencia, y
        # además puntúa. Caso real (batería 6, bol.com y allegro.pl): ambas
        # devuelven 403 a todo; el check 3.6 leyó la página de "Access Denied",
        # no encontró elementos fantasma en ella y se anotó un 1. Como 3.6 era
        # el ÚNICO check de C3 que sobrevivía, C3 quedó en 1.0 con el peso más
        # alto del modelo (25) y allegro.pl salió con 64.5 y nivel
        # "Agent-aware": la MEJOR nota de toda la batería para un sitio que no
        # hemos visto. Los sitios que más nos bloquean puntuaban más alto.
        # Solo aplica a los niveles donde el propio contenido no es creíble
        # ("total" y "marcado"); en "sondas" la portada sí se vio y un >0
        # procede de una sonda que sí respondió, así que se respeta.
        contenido_no_creible = (nivel in ("total", "marcado")
                                and r["id"] in CHECKS_DE_MARCADO)
        if r["score"] == 0 or (contenido_no_creible and r["score"] is not None):
            r["score"] = None
            r["manual"] = True
            # marca explícita: el scoring necesita distinguir "aquí no hay nada
            # que medir" (no aplica, su peso se reparte) de "no hemos podido
            # medirlo" (no se reparte, o inflaría lo poco que quede en pie)
            r["no_verificable"] = True
            r["evidence"] = f"NO VERIFICABLE — {motivo}. (Sonda: {r['evidence'][:120]})"
            n += 1
    ctx["acceso_degradado"] = {"nivel": nivel, "motivo": motivo, "degradados": n,
                               "human_status": human, "via": via}
    if n:
        aviso_final = ("La puntuación global queda marcada como NO FIABLE: no debe "
                       "entregarse sin repetir el análisis"
                       if nivel in ("total", "marcado") else
                       "La puntuación sigue siendo utilizable: cubre lo que sí se "
                       "pudo comprobar")
        ctx["trail"].append({
            "step": "guardarraíl de fidelidad", "status": "warn",
            "detail": f"{n} checks degradados a 'no verificable' (nivel {nivel}). "
                      f"{motivo}. {aviso_final}"})
    return results


def audit_domain(base, typology_override=None, skip_render=False, with_psi=False,
                 categories=None, with_agents=False, allow_submit=False,
                 check_ids=None, agent_repeticiones=3, agentes_pendientes=False):
    """Audita un dominio y devuelve el dict de resultados (apto para JSON/UI)."""
    base = discovery.normalize(base)
    assert_public_url(base)  # anti-SSRF antes de nada
    ctx = gather_context(base, typology_override, skip_render, with_psi)
    # las pruebas agenticas se lanzan aparte desde el informe: el check 6.3 lo
    # dice asi en vez de pedir que se active algo que ya esta activado
    ctx["agentes_pendientes"] = bool(agentes_pendientes) and not with_agents

    if with_agents:
        _log("pruebas agénticas reales (varios minutos)…")
        try:
            from .agents import run_agent_tests
            ctx["agent_tests"] = run_agent_tests(
                base, ctx["typology"], allow_submit=allow_submit, log=LOG_SINK,
                repeticiones=agent_repeticiones)
            agents = ctx["agent_tests"].get("agents", {})
            ran = [k for k, v in agents.items() if v.get("outcome") != "no_disponible"]
            det = (f"agentes ejecutados: {', '.join(ran) or 'ninguno'}"
                   f" · {ctx['agent_tests'].get('repeticiones')} intentos por agente")
            if allow_submit:
                det += " [envío de formularios AUTORIZADO en este dominio]"
            ctx["trail"].append({"step": "pruebas agénticas (6.3)",
                                 "status": "ok" if ran else "fail", "detail": det})
        except Exception as exc:
            ctx["agent_tests"] = None
            ctx["trail"].append({"step": "pruebas agénticas (6.3)", "status": "fail",
                                 "detail": str(exc)[:200]})
    else:
        ctx["trail"].append({"step": "pruebas agénticas (6.3)", "status": "skipped",
                             "detail": "desactivadas: el check 6.3 queda como manual"})

    # La sonda de rate limiting va la ULTIMA de todas a propósito: aporrear el
    # sitio con 10 peticiones seguidas al principio contaminaba las mediciones
    # posteriores (muestreo, render, sondas e incluso las pruebas agénticas) en
    # sitios con límite por IP. Detectado en calibración: perfumesclub devolvió
    # 429/418 al muestreo tras dispararse esta sonda, y acabamos baneados.
    _log("rate limiting (última sonda, para no contaminar el resto)…")
    ctx["rapid"] = rapid_fire(base + "/", BOT_UAS["GPTBot"], n=10)
    ctx["trail"].append({"step": "rate limiting", "status": "ok",
                         "detail": f"10 peticiones como GPTBot: {ctx['rapid']} "
                                   "(se ejecuta al final para no distorsionar las demás sondas)"})

    results = checks_mod.run_all(ctx)
    results = _degradar_sin_ficha_producto(results, ctx)
    results = _degradar_si_bloqueado(results, ctx)
    if categories:
        results = [r for r in results if r["cat"] in categories]
    if check_ids:
        results = [r for r in results if r["id"] in check_ids]

    total, cat_scores, weights, cobertura = scoring.total_score(results, ctx["typology"])
    adjusted, penalties = scoring.apply_governance_gate(total, results, ctx["typology"])
    level = scoring.level_for(adjusted, veredicto=_veredicto_de_acceso(ctx))

    from .knowledge import advice_for
    for r in results:
        if r["score"] is not None and r["score"] < 1:
            r["advice"] = advice_for(r["id"])

    return {
        "domain": base,
        "host": urlparse(base).netloc.replace("www.", ""),
        "typology": ctx["typology"],
        "typology_evidence": ctx["typology_evidence"],
        "score": adjusted,
        "score_pre_gate": total,
        "penalties": penalties,
        "level": level,
        "category_scores": cat_scores,
        "category_weights": {k: round(v, 1) for k, v in weights.items()},
        # qué parte del modelo cubre de verdad esta nota (1.0 = todo lo que
        # aplica). Baja cuando hay categorías que NO pudimos medir, y su peso
        # ya no se reparte sobre las demás para no inflarlas.
        "cobertura_score": cobertura,
        "checks": results,
        "bot_matrix": ctx["bot_matrix"],
        "buckets": ctx["buckets_size"],
        "pages_sampled": [{"url": p["url"], "bucket": p["bucket"],
                           "status": p["fetch"]["status"],
                           "via": p["fetch"].get("_via", "http")} for p in ctx["pages"]],
        "wellknown": {k: v for k, v in ctx["wellknown"].items() if v == 200},
        "jina_ok": ctx["jina_ok"],
        "render_ok": bool(ctx.get("rendered_home") and ctx["rendered_home"].get("ok")),
        # "sondas" degrada checks concretos pero la puntuación sigue valiendo;
        # solo "total" y "marcado" invalidan el score para entrega
        "score_fiable": (ctx.get("acceso_degradado") or {}).get("nivel")
                        not in ("total", "marcado"),
        "acceso_degradado": ctx.get("acceso_degradado"),
        # Presente solo si es una tienda cuyo catálogo no alcanzamos: la UI y el
        # PDF deben poder decir "C7 incompleto" en vez de "C7 suspende"
        "cobertura_producto_degradada": ctx.get("cobertura_producto_degradada"),
        "error_handling": ctx.get("error_probe"),
        "login_area": {k: v for k, v in (ctx.get("login_probe") or {}).items()
                       if k != "body"},
        "interactive_controls": len((ctx.get("rendered_home") or {}).get("boxes") or []),
        "agent_tests": ctx.get("agent_tests"),
        "trail": ctx["trail"],
        "coverage": {
            "sitemap_urls": len(ctx["sitemap"]["urls"]),
            "sampled": len(ctx["pages"]),
            "sampled_ok": sum(1 for p in ctx["pages"] if p["fetch"]["status"] == 200),
            "fallbacks": sum(1 for p in ctx["pages"] if p["fetch"].get("_via")),
            "buckets": ctx["buckets_size"],
        },
    }


def run_audit(urls, typology=None, skip_render=False, with_psi=False,
              categories=None, log=None):
    """Audita cliente + competidores. Devuelve el dict 'data' completo para la UI."""
    global LOG_SINK
    if log is not None:
        LOG_SINK = log
    audits = []
    try:
        for u in urls[:3]:
            try:
                audits.append(audit_domain(u, typology, skip_render, with_psi, categories))
            except Exception as exc:
                audits.append({"domain": u, "error": str(exc)[:200]})
    finally:
        LOG_SINK = None
    return {
        "client": audits[0] if audits else None,
        "competitors": audits[1:],
        "framework_version": "2.0 (agent_scanner, integrado en clicandseo)",
    }
