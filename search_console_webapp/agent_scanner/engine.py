def probe_wellknown(base):
    """Sondea rutas agénticas validando CONTENIDO (evita soft-404 de los CMS).

    Devuelve (códigos, metadatos). Los metadatos permiten juzgar CALIDAD y no
    solo presencia: un llms.txt de 900 KB autogenerado no vale lo mismo que uno
    de 30 KB curado, y medir solo "existe" invierte el ranking real.
    """
    out, meta = {}, {}
    for p in WELLKNOWN_PATHS:
        res = fetch(base + p, timeout=12)
        code = res["status"]
        if code == 200:
            body = (res["body"] or "").strip()
            is_html = "<html" in body[:400].lower() or "<!doctype" in body[:200].lower()
            text_paths = ("/llms.txt", "/llms-full.txt", "/auth.md", "/.well-known/skills")
            if p in text_paths or p.endswith(".md"):
                valid = not is_html and len(body) > 20
            elif p.endswith(".json") or p in ("/mcp", "/ask") or p.startswith("/.well-known/"):
                try:
                    json.loads(body)
                    valid = True
                except (json.JSONDecodeError, ValueError):
                    valid = not is_html and any(k in body[:500]
                                                for k in ("jsonrpc", "mcp", "openapi"))
            elif p == "/api-docs":
                valid = "swagger" in body.lower()[:3000] or "openapi" in body.lower()[:3000]
            else:
                valid = not is_html
            out[p] = 200 if valid else 0
            if valid:
                meta[p] = {
                    "bytes": len(body),
                    "lineas": body.count("\n") + 1,
                    "enlaces": body.count("](http") + body.count("- http"),
                    "autogenerado": bool(re.search(
                        r"(?i)(generado (por|con)|generated by|auto-?generated|"
                        r"all in one seo|yoast|rank ?math|plugin)", body[:600])),
                }
        else:
            out[p] = code
    return out, meta


def probe_error_handling(base):
    """Sonda de estados de error: pide una URL que no puede existir.

    Un soft-404 (HTTP 200 en una página inexistente) es un fallo agéntico grave:
    el agente cree que la operación fue bien y sigue con datos inventados. Un
    humano lee "no encontrado"; el agente solo mira el código de estado.
    """
    probe = base + "/agent-readiness-probe-404-no-existe-xyz"
    res = fetch(probe, timeout=15, ua=UA_HUMAN)
    body = res["body"] or ""
    text = re.sub(r"(?is)<(script|style).*?</\1>|<[^>]+>", " ", body)
    looks_missing = bool(re.search(
        r"(?i)no (se ha |hemos )?(encontrado|existe)|not found|404|"
        r"página no disponible|pagina no encontrada|error 404", text[:4000]))
    # ¿ofrece salida? (buscador o navegación para que el agente se recupere)
    has_recovery = bool(re.search(r'(?i)<input[^>]+type=["\']search|role=["\']search|'
                                  r'<nav\b|<form[^>]+(search|buscar)', body))
    return {"status": res["status"], "looks_missing": looks_missing,
            "has_recovery": has_recovery, "url": probe}


LOGIN_URL_RE = re.compile(
    r'href=["\']([^"\']*/(login|signin|sign-in|iniciar-sesion|acceso|'
    r'mi-cuenta|my-account|account)[^"\']*)["\']', re.I)


def _es_ficha_producto(valid_blocks):
    """¿Esta página es una FICHA de producto, o un artículo que lo menciona?

    La prensa incrusta Product schema en piezas de afiliación ("los mejores
    ventiladores"): eldiario.es acababa clasificado como e-commerce por dos
    noticias en catalán. Dos exigencias para aceptarla como ficha:
      1. el Product debe ser vendible (offers con precio), no una mención;
      2. la página no debe declararse a la vez como artículo — si lleva
         NewsArticle/Article/BlogPosting, es contenido editorial.
    """
    if checks_mod.find_nodes(valid_blocks, "NewsArticle") \
            or checks_mod.find_nodes(valid_blocks, "Article") \
            or checks_mod.find_nodes(valid_blocks, "BlogPosting") \
            or checks_mod.find_nodes(valid_blocks, "LiveBlogPosting"):
        return False
    for prod in checks_mod.find_nodes(valid_blocks, "Product"):
        offers = prod.get("offers") or {}
        if isinstance(offers, list):
            offers = offers[0] if offers else {}
        if isinstance(offers, dict) and (offers.get("price")
                                         or offers.get("priceCurrency")
                                         or offers.get("lowPrice")):
            return True
    return False


def _trail_tipologia(ctx, typology_override):
    """Deja en el trail cómo se ha clasificado el sitio, y por qué.

    Vive fuera de gather_context para poder probarse sin red: el matiz que
    distingue "no hay señales" de "no hemos podido mirar" es justo el que se
    coló en producción (ver test_sin_senales_no_culpa_al_acceso).
    """
    def T(status, detail):
        ctx["trail"].append({"step": "detección de tipología", "status": status,
                             "detail": str(detail)[:300]})

    if typology_override:
        T("ok", f"{ctx['typology']} (forzada por configuración)")
        return

    typ_ev = ctx.get("typology_evidence") or {}
    pts_e = (typ_ev.get("ecommerce") or {}).get("puntos", 0)
    pts_s = (typ_ev.get("saas") or {}).get("puntos", 0)
    det = (f"{ctx['typology']} — e-commerce {pts_e} pts "
           f"{(typ_ev.get('ecommerce') or {}).get('fuertes')}, saas {pts_s} pts "
           f"{(typ_ev.get('saas') or {}).get('fuertes')}")
    if pts_e or pts_s:
        T("ok", det)
        return

    # "Cero señales" tenía UNA sola lectura ("el sitio nos bloquea") y se
    # afirmaba siempre. Caso real (batería 6): dnb.no nos sirvió 1,17 MB de
    # portada por HTTP 200 y aun así el informe decía "la web devuelve
    # poco/ningún contenido a accesos automatizados". Igual en rijksoverheid.nl
    # (318 KB), vg.no (431 KB), uu.nl y greenpeace.nl. Un banco, un ministerio,
    # una universidad, un periódico y una ONG no tienen señales de tienda ni de
    # SaaS porque NO son eso: la ausencia es correcta y el diagnóstico, falso.
    # Solo se culpa al acceso cuando de verdad no hemos podido leer la portada.
    home = ctx.get("home") or {}
    hb = home.get("body") or ""
    portada_vista = (len(hb) >= 500 and (home.get("status") == 200
                                         or home.get("_via") in ("render", "jina")))
    if portada_vista:
        T("ok", det + " — sin señales de e-commerce ni de SaaS en una portada que "
                      f"sí se leyó ({len(hb)} bytes): se clasifica como 'corporativo', "
                      "que es lo normal en banca, sector público, medios y ONG. "
                      "Si no lo es, fuerza la tipología en el formulario.")
    else:
        T("warn", det + " — SIN señales: la web devuelve poco/ningún contenido a "
                        "accesos automatizados. Se asume 'corporativo' por defecto; "
                        "si no lo es, fuerza la tipología en el formulario.")


def _abs_url(base, url):
    if url.startswith("//"):
        return "https:" + url
    if url.startswith("/"):
        return base + url
    if url.startswith("http"):
        return url
    return None


def probe_login(base, home_html, skip_render=False):
    """Localiza el área de acceso y la trae, para evaluar si un agente podría
    autenticarse (check 6.4). Si el sitio no tiene login, no es un fallo: es N/A.

    Prueba VARIOS candidatos (no solo el primer enlace) y, si el formulario no
    está en el HTML crudo, renderiza: hoy muchos logins se pintan con JS y
    quedarnos en el crudo daría un "no evaluable" que en realidad sí lo es.
    """
    host = urlparse(base).netloc.replace("www.", "")
    cands, seen = [], set()
    for m in LOGIN_URL_RE.finditer(home_html or ""):
        u = _abs_url(base, m.group(1))
        if not u or u in seen:
            continue
        if urlparse(u).netloc.replace("www.", "") != host:
            continue
        seen.add(u)
        cands.append(u)
        if len(cands) >= 4:
            break
    # rutas convencionales por si la home no enlaza el acceso
    cands += [base + p for p in ("/login", "/cuenta", "/mi-cuenta")
              if base + p not in seen]
    if not cands:
        return {"found": False}

    intentos, mejor = [], None
    for u in cands[:5]:
        res = fetch(u, timeout=20, ua=UA_HUMAN)
        body = res["body"] or ""
        has_pwd = bool(re.search(r'(?i)<input[^>]+type=["\']password', body))
        intentos.append({"url": u, "status": res["status"], "password_en_crudo": has_pwd})
        if res["status"] == 200 and has_pwd:
            return {"found": True, "url": u, "status": res["status"], "body": body,
                    "via": "http", "intentos": intentos}
        if res["status"] == 200 and mejor is None:
            mejor = {"url": u, "status": res["status"], "body": body}
    if mejor is None:
        return {"found": False, "intentos": intentos}
    # ningún candidato mostró el formulario en crudo: puede ser un login con JS
    if not skip_render:
        r = render_page(mejor["url"], timeout=60)
        if r.get("ok") and re.search(r'(?i)<input[^>]+type=["\']password', r.get("html") or ""):
            return {"found": True, "url": mejor["url"], "status": mejor["status"],
                    "body": r["html"], "via": "render", "intentos": intentos}
    return {"found": True, "url": mejor["url"], "status": mejor["status"],
            "body": mejor["body"], "via": "http", "intentos": intentos}


