"""
Manual AI — Professional server-side PDF export (AI Overview Visibility Report).

Generates a multi-page, branded PDF report that mirrors the entire Analytics
tab of the Manual AI dashboard at /manual-ai/. Designed to be "digno de
enviar a tu jefe o a tu cliente" — a report you can hand to a paying client
as-is.

Architecture mirrors the LLM Monitoring PDF export in `llm_monitoring_routes.py`
(`export_project_pdf`, lines 5635-6668) but with a cleaner service layer.

## Page structure (9 pages, 8 if clusters disabled)

    1. Cover / Project Details
    2. Executive Summary (6 KPIs)
    3. Trends Over Time (visibility chart + position distribution + events)
    4. Thematic Clusters Analysis    (skipped entirely if disabled)
    5. Competitive Analysis           (multi-series charts + comparison table)
    6. AI Overview Keywords Details   (dynamic competitor columns)
    7. Top Mentioned URLs in AI Overview
    8. Global AI Overview Domains Ranking
    9. AI Overview vs Organic Comparison  (the new flagship feature)

## Brand styling

Every page has a dark header bar (#161616) with the report title and a
Clicandseo logo watermark on the right, plus a footer with page number and
logo. Tables use alternating row colors, dark headers with white text,
colored cells for sentiment-like signals (green/yellow/red), and clickable
URL links in blue.

## Data sources (all read-only, no writes)

    - ProjectRepository.get_project_info
    - StatisticsService.get_project_statistics
    - StatisticsService.get_latest_overview_stats
    - StatisticsService.get_project_ai_overview_keywords_latest
    - StatisticsService.get_project_urls_ranking
    - StatisticsService.get_project_global_domains_ranking
    - StatisticsService.get_aio_vs_organic_comparison
    - ClusterService.get_cluster_statistics
    - CompetitorService.get_project_comparative_charts_data

All fetches are wrapped in try/except so a single data source failing does
not crash the whole PDF — the affected page renders a "No data available"
placeholder instead.
"""

