    @staticmethod
    def _fetch_all_data(project_id: int, days: int) -> Dict:
        """
        Fetch everything the PDF needs in one go. Every call is wrapped in
        try/except — if a service fails, the relevant key is set to a
        sensible empty value and the affected page builder renders a
        "No data" placeholder instead of crashing.
        """
        from manual_ai.models.project_repository import ProjectRepository
        from manual_ai.services.statistics_service import StatisticsService
        from manual_ai.services.cluster_service import ClusterService
        from manual_ai.services.competitor_service import CompetitorService

        data: Dict = {
            'project_id': project_id,
            'days': days,
            'project_info': None,
            'latest_overview': {},
            'project_stats': {},
            'clusters': {},
            'comparative': {},
            'keywords_latest': {},
            'top_urls': [],
            'global_domains': [],
            'aio_vs_organic': {},
        }

        try:
            data['project_info'] = ProjectRepository.get_project_info(project_id)
        except Exception as e:
            logger.error(f"[PDF Export] Failed to fetch project_info: {e}")
            return data

        if not data['project_info']:
            return data

        # StatisticsService calls
        try:
            data['latest_overview'] = StatisticsService.get_latest_overview_stats(project_id) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_latest_overview_stats failed: {e}")

        try:
            data['project_stats'] = StatisticsService.get_project_statistics(project_id, days) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_project_statistics failed: {e}")

        try:
            data['keywords_latest'] = StatisticsService.get_project_ai_overview_keywords_latest(project_id) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_project_ai_overview_keywords_latest failed: {e}")

        try:
            data['top_urls'] = StatisticsService.get_project_urls_ranking(project_id, days, limit=20) or []
        except Exception as e:
            logger.warning(f"[PDF Export] get_project_urls_ranking failed: {e}")

        try:
            data['global_domains'] = StatisticsService.get_project_global_domains_ranking(project_id, days) or []
        except Exception as e:
            logger.warning(f"[PDF Export] get_project_global_domains_ranking failed: {e}")

        try:
            data['aio_vs_organic'] = StatisticsService.get_aio_vs_organic_comparison(project_id, days) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_aio_vs_organic_comparison failed: {e}")

        # ClusterService
        try:
            data['clusters'] = ClusterService.get_cluster_statistics(project_id, days) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_cluster_statistics failed: {e}")

        # CompetitorService
        try:
            data['comparative'] = CompetitorService.get_project_comparative_charts_data(project_id, days) or {}
        except Exception as e:
            logger.warning(f"[PDF Export] get_project_comparative_charts_data failed: {e}")

        return data

