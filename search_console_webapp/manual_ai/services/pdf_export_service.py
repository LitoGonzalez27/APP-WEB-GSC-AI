    @staticmethod
    def _build_visibility_linechart(visibility_data: List[Dict], styles: Dict):
        """Build a single-series line chart for domain visibility over time."""
        from reportlab.graphics.shapes import Drawing, Rect, String
        from reportlab.graphics.charts.lineplots import LinePlot
        from reportlab.graphics.widgets.markers import makeMarker
        from reportlab.lib import colors
        from reportlab.lib.units import cm

        # Filter rows with numeric visibility
        points = []
        dates = []
        for i, row in enumerate(visibility_data):
            pct = row.get('visibility_pct')
            if pct is None:
                continue
            try:
                points.append((i, float(pct)))
                dates.append(str(row.get('analysis_date', '')))
            except (TypeError, ValueError):
                continue

        if len(points) < 2:
            return None

        chart_w = 17 * cm
        chart_h = 7 * cm
        drawing = Drawing(float(chart_w), float(chart_h))

        # Background
        drawing.add(Rect(
            0, 0, float(chart_w), float(chart_h),
            fillColor=colors.HexColor('#FAFAFA'), strokeColor=None,
        ))

        # Re-normalize x values to 0-based index after filtering
        plot_data = [[(new_i, y) for new_i, (_, y) in enumerate(points)]]

        lp = LinePlot()
        lp.x = 40
        lp.y = 30
        lp.width = float(chart_w) - 60
        lp.height = float(chart_h) - 55
        lp.data = plot_data

        lp.lines[0].strokeColor = colors.HexColor(_CHART_COLOR_PROJECT)
        lp.lines[0].strokeWidth = 2.5
        lp.lines[0].symbol = makeMarker('Circle')
        lp.lines[0].symbol.size = 4
        lp.lines[0].symbol.fillColor = colors.HexColor(_CHART_COLOR_PROJECT)

        n = len(points)
        tick_step = max(1, n // 8)
        label_format_dates = dates  # closure reference for the lambda

        lp.xValueAxis.valueMin = 0
        lp.xValueAxis.valueMax = max(n - 1, 1)
        lp.xValueAxis.valueSteps = list(range(0, n, tick_step))
        lp.xValueAxis.labelTextFormat = (
            lambda v, _d=label_format_dates:
            _d[int(v)][-5:] if 0 <= int(v) < len(_d) else ''
        )
        lp.xValueAxis.labels.fontSize = 7
        lp.xValueAxis.labels.angle = 0
        lp.xValueAxis.strokeColor = colors.HexColor('#E5E7EB')

        lp.yValueAxis.valueMin = 0
        lp.yValueAxis.valueMax = 100
        lp.yValueAxis.valueStep = 20
        lp.yValueAxis.labelTextFormat = '%d%%'
        lp.yValueAxis.labels.fontSize = 7
        lp.yValueAxis.strokeColor = colors.HexColor('#E5E7EB')
        lp.yValueAxis.gridStrokeColor = colors.HexColor('#F3F4F6')
        lp.yValueAxis.visibleGrid = 1

        drawing.add(lp)

        # Legend at bottom-left
        drawing.add(Rect(40, 5, 8, 8, fillColor=colors.HexColor(_CHART_COLOR_PROJECT), strokeColor=None))
        drawing.add(String(52, 6, 'VISIBILITY %', fontSize=7, fillColor=colors.HexColor(_CLR_LIGHT_GRAY)))

        return drawing

