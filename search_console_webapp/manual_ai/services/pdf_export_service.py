    @staticmethod
    def _base_table_style(num_rows: int, styles: Dict) -> list:
        """
        Standard dark-header table style with alternating rows.
        Callers can append their own overrides.
        """
        cmds = [
            ('BACKGROUND', (0, 0), (-1, 0), styles['CLR_DARK']),
            ('TEXTCOLOR', (0, 0), (-1, 0), styles['CLR_WHITE']),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 8),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 8),
            ('TOPPADDING', (0, 0), (-1, 0), 8),
            ('TEXTCOLOR', (0, 1), (-1, -1), styles['CLR_BODY']),
            ('FONTNAME', (0, 1), (-1, -1), 'Helvetica'),
            ('FONTSIZE', (0, 1), (-1, -1), 8),
            ('GRID', (0, 0), (-1, -1), 0.5, styles['CLR_BORDER']),
            ('TOPPADDING', (0, 1), (-1, -1), 6),
            ('BOTTOMPADDING', (0, 1), (-1, -1), 6),
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ]
        # Alternating row colors
        for i in range(1, num_rows):
            if i % 2 == 0:
                cmds.append(('BACKGROUND', (0, i), (-1, i), styles['CLR_ROW_ALT']))
        return cmds

