    @staticmethod
    def _truncate(text, max_len: int = 60) -> str:
        """Truncate text to max_len with ellipsis."""
        if text is None:
            return 'N/A'
        text = str(text)
        if len(text) <= max_len:
            return text
        return text[:max_len - 3] + '...'

    @staticmethod
    def _escape_html(text) -> str:
        """Escape text for ReportLab Paragraph (HTML-like markup)."""
        if text is None:
            return ''
        s = str(text)
        return (
            s.replace('&', '&amp;')
             .replace('<', '&lt;')
             .replace('>', '&gt;')
        )

    @staticmethod
    def _fmt_num(value, decimals: int = 0, default: str = 'N/A') -> str:
        """Format a number to a fixed number of decimals, handling None."""
        if value is None:
            return default
        try:
            v = float(value)
            if decimals == 0:
                return f"{int(round(v)):,}"
            return f"{v:.{decimals}f}"
        except (TypeError, ValueError):
            return default

    @staticmethod
    def _fmt_pct(value, decimals: int = 1, default: str = 'N/A') -> str:
        """Format a value as percentage."""
        if value is None:
            return default
        try:
            return f"{float(value):.{decimals}f}%"
        except (TypeError, ValueError):
            return default

    @staticmethod
    def _domain_of(url: str) -> Optional[str]:
        """Extract a canonical domain from a URL (no scheme, no www)."""
        if not url:
            return None
        try:
            parsed = urlparse(url if '://' in url else f'https://{url}')
            host = (parsed.netloc or '').lower()
            if host.startswith('www.'):
                host = host[4:]
            return host or None
        except Exception:
            return None

    @staticmethod
    def _clusters_have_data(clusters_data: Dict) -> bool:
        """Return True if the clusters page should be rendered."""
        if not clusters_data:
            return False
        if not clusters_data.get('enabled'):
            return False
        if not clusters_data.get('table_data'):
            return False
        return True

