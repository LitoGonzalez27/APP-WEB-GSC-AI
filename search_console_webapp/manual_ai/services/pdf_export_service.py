class ManualAiPdfExportService:
    """Generate a professional multi-page PDF report for a Manual AI project."""

    @staticmethod
    def generate_project_pdf(project_id: int, days: int = 30) -> BytesIO:
        """
        Build the full Manual AI PDF for a project and return it as a BytesIO.

        Args:
            project_id: Manual AI project ID
            days:       Lookback window in days (default 30, clamped to [1, 365])

        Returns:
            BytesIO positioned at 0 — ready to be passed to Flask's `send_file`.

        Raises:
            ImportError: if ReportLab is not installed (requirements.txt already
                        pins `reportlab==4.2.5`)
            ValueError:  if the project does not exist
        """
        # Validate params
        if days < 1:
            days = 1
        elif days > 365:
            days = 365

        logger.info(f"[PDF Export] Starting generation for project {project_id}, days={days}")

        # Import ReportLab lazily so `import manual_ai.services.pdf_export_service`
        # doesn't fail in environments where reportlab is missing (e.g. some
        # test setups). The route handler already catches ImportError.
        from reportlab.lib import colors
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.units import cm
        from reportlab.platypus import SimpleDocTemplate

        # Fetch all data (defensive — one source failing does not kill the PDF)
        data = ManualAiPdfExportService._fetch_all_data(project_id, days)
        if not data.get('project_info'):
            raise ValueError(f"Project {project_id} not found")

        # Build styles
        styles = ManualAiPdfExportService._build_styles()

        # Build the document
        page_width, page_height = A4
        output = BytesIO()

        doc = SimpleDocTemplate(
            output,
            pagesize=A4,
            rightMargin=2 * cm,
            leftMargin=2 * cm,
            topMargin=2.2 * cm,
            bottomMargin=1.8 * cm,
            title=_REPORT_TITLE,
            author='ClicAndSEO',
            subject=f"AI Overview report — {data['project_info'].get('name', 'Project')}",
        )

        # Page chrome closures (need access to page_width / page_height)
        def _on_page(canvas_obj, doc_obj):
            ManualAiPdfExportService._draw_page_chrome(
                canvas_obj, doc_obj, page_width, page_height
            )

        elements = []

        # Build each page
        try:
            ManualAiPdfExportService._build_page1_cover(elements, data, styles, days)
            ManualAiPdfExportService._build_page2_executive_summary(elements, data, styles)
            ManualAiPdfExportService._build_page3_trends(elements, data, styles)

            # Page 4 is optional
            if ManualAiPdfExportService._clusters_have_data(data.get('clusters')):
                ManualAiPdfExportService._build_page4_clusters(elements, data, styles)

            ManualAiPdfExportService._build_page5_competitive(elements, data, styles)
            ManualAiPdfExportService._build_page6_keywords(elements, data, styles)
            ManualAiPdfExportService._build_page7_top_urls(elements, data, styles)
            ManualAiPdfExportService._build_page8_domains_ranking(elements, data, styles)
            ManualAiPdfExportService._build_page9_aio_vs_organic(elements, data, styles)
        except Exception as page_error:
            # If a page builder raises despite defensive handling, log and
            # continue — partial PDF is better than no PDF at all.
            logger.error(f"[PDF Export] Page builder error: {page_error}", exc_info=True)

        doc.build(elements, onFirstPage=_on_page, onLaterPages=_on_page)
        output.seek(0)

        size_kb = output.getbuffer().nbytes / 1024
        logger.info(f"[PDF Export] Generation complete for project {project_id}: {size_kb:.1f} KB")

        return output

