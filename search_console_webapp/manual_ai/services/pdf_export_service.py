    @staticmethod
    def _build_page1_cover(elements: list, data: Dict, styles: Dict, days: int):
        from reportlab.lib.units import cm
        from reportlab.platypus import Paragraph, Spacer, Table, TableStyle

        project = data['project_info']
        clusters = data.get('clusters') or {}
        latest = data.get('latest_overview') or {}

        name = project.get('name', 'Project')
        description = project.get('description') or ''
        domain = project.get('domain', 'N/A')
        country = project.get('country_code', 'N/A')
        competitors = project.get('selected_competitors') or []
        clusters_enabled = bool(clusters.get('enabled'))
        clusters_count = int(clusters.get('total_clusters') or 0) if clusters_enabled else 0
        last_analysis = latest.get('last_analysis_date')

        elements.append(Spacer(1, 0.5 * cm))
        elements.append(Paragraph(_REPORT_TITLE, styles['title']))
        elements.append(Spacer(1, 0.3 * cm))
        elements.append(Paragraph(
            ManualAiPdfExportService._escape_html(name), styles['project']
        ))
        elements.append(Paragraph(
            f"Period: Last {days} days  |  Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}",
            styles['period']
        ))

        if description:
            elements.append(Paragraph(
                ManualAiPdfExportService._escape_html(description),
                styles['description']
            ))

        elements.append(Spacer(1, 0.6 * cm))

        # Project Details table
        elements.append(Paragraph("Project Details", styles['section']))

        details_rows = [
            ['Field', 'Value'],
            ['Domain', domain or 'N/A'],
            ['Country', country or 'N/A'],
            ['Period analyzed', f"Last {days} days"],
            ['Competitors tracked', str(len(competitors))],
            ['Thematic clusters',
             f"Enabled ({clusters_count} clusters)" if clusters_enabled else 'Disabled'],
            ['Last analysis',
             str(last_analysis) if last_analysis else 'No data yet'],
        ]

        details_table = Table(details_rows, colWidths=[5 * cm, 10 * cm])
        ds = ManualAiPdfExportService._base_table_style(len(details_rows), styles)
        ds.append(('ALIGN', (0, 1), (0, -1), 'LEFT'))
        ds.append(('ALIGN', (1, 1), (1, -1), 'LEFT'))
        ds.append(('FONTNAME', (0, 1), (0, -1), 'Helvetica-Bold'))
        details_table.setStyle(TableStyle(ds))
        elements.append(details_table)
        elements.append(Spacer(1, 0.6 * cm))

        # Competitors section
        if competitors:
            elements.append(Paragraph("Competitors Tracked", styles['subsection']))
            elements.append(Spacer(1, 0.15 * cm))

            comp_rows = [['#', 'Domain']]
